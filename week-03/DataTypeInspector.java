
public class DataTypeInspector
{
    public static void main(String [] args) {
        byte num1= 120;
        short num2= 560;
        int num3= 100020000;
        long num4= 34544348778989L;
        float num5= 23432.4F;
        double num6= 34879933.789D;
        boolean a= true;
        char b = 'P';
        
        System.out.println("The value in byte data type is: " +num1);
        System.out.println("The value in short data type is: " +num2);
        System.out.println("The value in int data type is: " + num3);
        System.out.println("The value in long data type is: " +num4);
        System.out.println("The value in float data type is: " +num5);
        System.out.println("The value in double data type is: " + num6);
        System.out.println("The value in boolean data type is: " +a);
        System.out.println("The value in char data type is: " +b);
    }
}